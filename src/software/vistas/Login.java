    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        Usuario usrLogin = this.contInicio.loginAplicacion(String.valueOf(txfUsuario.getText()), String.valueOf(txfClave.getText()));
        if (usrLogin != null && usrLogin.isActivo() == true) {
            this.confApp.setDatosUsuario(usrLogin);
            Contenedor c = new Contenedor(this.confApp);
            c.setVisible(true);
            c.setLocationRelativeTo(null);
            this.dispose();
        } else {
            txfUsuario.setText("");
            txfClave.setText("");
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

