    private void tblAdminOperadoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAdminOperadoresMouseClicked
        int filaSeleccionada = tblAdminOperadores.rowAtPoint(evt.getPoint());
        int columnaSeleccionada = tblAdminOperadores.columnAtPoint(evt.getPoint());
        if (columnaSeleccionada < 4) {
            this.contAdmin.selecionarOperador(txfOpNombre, txfOpApellido, txfOpDocumento, chkOpAdmin, txfOpUsuario, txfOpClave, txfOpConfClave, txfOpCodigo, filaSeleccionada, columnaSeleccionada, this.confApp.getListaOperadores());
        } else {
            ArrayList<Usuario> listaOperadores = this.contAdmin.actualizarEstadoOperador(filaSeleccionada, columnaSeleccionada, this.confApp.getListaOperadores());
            if (listaOperadores != null && !listaOperadores.isEmpty()) {
                this.confApp.setListaOperadores(listaOperadores);
            }
        }
    }//GEN-LAST:event_tblAdminOperadoresMouseClicked

