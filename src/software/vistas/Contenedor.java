    private void cmbAprietesHerramientasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbAprietesHerramientasItemStateChanged
        if (evt.getStateChange() == 2) {
            if (cmbAprietesHerramientas.getSelectedIndex() > 0) {
                this.confApp.setHerramientaActiva(this.confApp.getHerramientas().get(cmbAprietesHerramientas.getSelectedIndex() - 1));
                this.contAprietes.cargarAprietes(tblAprietesAprietes, this.confApp.getHerramientaActiva());
            } else {
                this.confApp.setHerramientaActiva(null);
            }
        }
    }//GEN-LAST:event_cmbAprietesHerramientasItemStateChanged

