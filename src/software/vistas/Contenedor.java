    private void txfHerrMedidaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txfHerrMedidaKeyTyped
        char caracter = evt.getKeyChar();
        if ((caracter < '0' || caracter > '9') && (caracter < '.' || caracter > '.' || String.valueOf(txfHerrMedida.getText()).contains(".")))
            evt.consume();
    }//GEN-LAST:event_txfHerrMedidaKeyTyped

